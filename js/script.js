			function DOMDisplay(parent, level) {
				this.wrap = parent.appendChild(element("div", "game"));
				this.level = level;
			
				this.wrap.appendChild(this.drawBackground());
				this.actorLayer = null;
				this.drawFrame();
			}
			
			var scale = 15;
			
			DOMDisplay.prototype.drawBackground = function () {
				var table = element("table", "background");
				table.style.width = this.level.width * scale + "px";
				table.style.height = this.level.height * scale + "px";
				this.level.grid.forEach(function (row) {
					var rowElement = table.appendChild(element("tr"));
					rowElement.style.height = scale + "px";
					row.forEach(function (type) {
						rowElement.appendChild(element("td", type));
					});
				});
				return table;
			};
			
			DOMDisplay.prototype.drawActors = function () {
				var wrap = element("div");
				this.level.actors.forEach(function (actor) {
					var rect = wrap.appendChild(element("div", "actor " + actor.type));
					rect.style.width = actor.size.x * scale + "px";
					rect.style.height = actor.size.y * scale + "px";
					rect.style.left = actor.pos.x * scale + "px";
					rect.style.top = actor.pos.y * scale + "px";
				});
				return wrap;
			};
			
			DOMDisplay.prototype.drawFrame = function () {
				if (this.actorLayer) this.wrap.removeChild(this.actorLayer);
				this.actorLayer = this.wrap.appendChild(this.drawActors());
				this.wrap.className = "game " + (this.level.status || "");
				this.scrollPlayerIntoView();
			};
			
			// 
			DOMDisplay.prototype.scrollPlayerIntoView = function () {
				var width = this.wrap.clientWidth;
				var height = this.wrap.clientHeight;
				var margin = width / 3;
			
				// The viewport
				var left = this.wrap.scrollLeft,
					right = left + width;
				var top = this.wrap.scrollTop,
					bottom = top + height;
			
				var player = this.level.player;
				var center = player.pos.plus(player.size.times(0.5)).times(scale);
			
				if (center.x < left + margin) this.wrap.scrollLeft = center.x - margin;
				else if (center.x > right - margin)
					this.wrap.scrollLeft = center.x + margin - width;
				if (center.y < top + margin) this.wrap.scrollTop = center.y - margin;
				else if (center.y > bottom - margin)
					this.wrap.scrollTop = center.y + margin - height;
			};
			
			DOMDisplay.prototype.clear = function () {
				this.wrap.parentNode.removeChild(this.wrap);
			};
			
		
			Level.prototype.obstacleAt = function (pos, size) {
				var xStart = Math.floor(pos.x);
				var xEnd = Math.ceil(pos.x + size.x);
				var yStart = Math.floor(pos.y);
				var yEnd = Math.ceil(pos.y + size.y);
			
				if (xStart < 0 || xEnd > this.width || yStart < 0) return "wall";
				if (yEnd > this.height) return "lava";
			
				for (var y = yStart; y < yEnd; y++) {
					for (var x = xStart; x < xEnd; x++) {
						var fieldType = this.grid[y][x];
						if (fieldType) {
							// console.log(`Obstacle detected: ${fieldType} at (${x}, ${y})`);
							return fieldType;
						}
					}
				}
				return null;
			};
			
			
			Level.prototype.actorAt = function (actor) {
				for (var i = 0; i < this.actors.length; i++) {
					var other = this.actors[i];
					if (other != actor &&
						actor.pos.x + actor.size.x > other.pos.x &&
						actor.pos.x < other.pos.x + other.size.x &&
						actor.pos.y + actor.size.y > other.pos.y &&
						actor.pos.y < other.pos.y + other.size.y) {
						// console.log(`Collision detected: ${actor.type} at (${actor.pos.x.toFixed(2)}, ${actor.pos.y.toFixed(2)}) with ${other.type} at (${other.pos.x.toFixed(2)}, ${other.pos.y.toFixed(2)})`);
						return other;
					}
				}
				return null;
			};
			
			
			var maxStep = 0.05;
		
			Level.prototype.animate = function (step, keys) {
				if (this.status != null) this.finishDelay -= step;
			
				while (step > 0) {
					var thisStep = Math.min(step, maxStep);
					this.actors.forEach(function (actor) {
						actor.act(thisStep, this, keys); //  act
					}, this);
					step -= thisStep;
				}
			};
			
			
			Lava.prototype.act = function (step, level) {
				var newPos = this.pos.plus(this.speed.times(step));
				var obstacle = level.obstacleAt(newPos, this.size);
			
				if (!obstacle) {
					this.pos = newPos; 
				} else if (this.repeatPos) {
					this.pos = this.repeatPos;
				} else {
					this.speed = this.speed.times(-1);
				}
			
			};
			
			
			var wobbleSpeed = 8,
				wobbleDist = 0.07;
			
			Coin.prototype.act = function (step) {
				this.wobble += step * wobbleSpeed;
				var wobblePos = Math.sin(this.wobble) * wobbleDist;
				this.pos = this.basePos.plus(new Vector(0, wobblePos));
			};
			
			var playerXSpeed = 10;
			
			Player.prototype.moveX = function (step, level, keys) {
				this.speed.x = 0;
				if (keys.left) this.speed.x -= playerXSpeed;
				if (keys.right) this.speed.x += playerXSpeed;
			
				var motion = new Vector(this.speed.x * step, 0);
				var newPos = this.pos.plus(motion);
				var obstacle = level.obstacleAt(newPos, this.size);
				if (obstacle) level.playerTouched(obstacle);
				else this.pos = newPos;
			};
			
			var gravity = 30;
			var jumpSpeed = 17;
			
			Player.prototype.moveY = function (step, level, keys) {
				this.speed.y += step * gravity;
				var motion = new Vector(0, this.speed.y * step);
				var newPos = this.pos.plus(motion);
				var obstacle = level.obstacleAt(newPos, this.size);
				if (obstacle) {
					level.playerTouched(obstacle);
					if (keys.up && this.speed.y > 0) this.speed.y = -jumpSpeed;
					else this.speed.y = 0;
				} else {
					this.pos = newPos;
				}
			};
			
			Player.prototype.act = function (step, level, keys) {
				this.moveX(step, level, keys);
				this.moveY(step, level, keys);
			
				var otherActor = level.actorAt(this);
				if (otherActor) {
					console.log(`Player is touching: ${otherActor.type}`);
					level.playerTouched(otherActor.type, otherActor);
				}
			
				if (level.status == "lost") {
					this.pos.y += step;
					this.size.y -= step;
				}
			};
			
		
			Level.prototype.playerTouched = function (type, actor) {
				if (type == "lava" && this.status == null) {
					// console.log(`Player touched lava at (${actor.pos.x.toFixed(2)}, ${actor.pos.y.toFixed(2)})`);
					this.status = "lost";
					this.finishDelay = 1;
				}else if (type == "Lava" && this.status == null) {
					// console.log(`Player touched lava at (${actor.pos.x.toFixed(2)}, ${actor.pos.y.toFixed(2)})`);
					this.status = "lost";
					this.finishDelay = 1;
				} 
				else if (type == "coin") {
					this.actors = this.actors.filter(function (other) {
						return other != actor;
					});
					if (!this.actors.some(function (actor) { return actor.type == "coin"; })) {
						this.status = "won";
						this.finishDelay = 1;
					}
				}
			};
			
			var arrowCodes = { 37: "left", 38: "up", 39: "right" };
			
			function trackKeys(codes) {
				var pressed = Object.create(null);
				function handler(event) {
					if (codes.hasOwnProperty(event.keyCode)) {
						var down = event.type == "keydown";
						pressed[codes[event.keyCode]] = down;
						event.preventDefault();
					}
				}
				addEventListener("keydown", handler);
				addEventListener("keyup", handler);
				return pressed;
			}
			
			function runAnimation(frameFunc) {
				var lastTime = null;
				function frame(time) {
					var stop = false;
					if (lastTime != null) {
						var timeStep = Math.min(time - lastTime, 100) / 1000;
						stop = frameFunc(timeStep) === false;
					}
					lastTime = time;
					if (!stop) requestAnimationFrame(frame);
				}
				requestAnimationFrame(frame);
			}
			
			var arrows = trackKeys(arrowCodes);
			
			
			function runLevel(level, Display, andThen) {
				var display = new Display(document.body, level);
				runAnimation(function (step) {
					level.animate(step, arrows);
					display.drawFrame(step);
					if (level.isFinished()) {
						display.clear();
						if (andThen) andThen(level.status);
						return false;
					}
				});
			}
			
